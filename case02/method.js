function add(x, y) {
	return x + y;
}

function sub(x, y) {
	return x - y;
}

devide = function(x, y) {
	// 簡單的 x 除以 y
	return x / y;
}

/**
 * 回傳 x 乘 y
 * @param x
 * @param y
 * @returns
 */
function multipy(x, y) {
	// 乘法後回傳
	return x * y;
}

function toDateString(date) {
	return date.format('yyyy-mm-dd');
}